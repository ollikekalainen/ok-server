"use strict";

const okserver = require("/www/ok-server");

okserver.defineApiRequest([ 
	{ name: "myIpAddress", parameters: {}, worker: __myIpAddress }
]);

// ---------------------------------------------------------------------------------------
// Workers
// ---------------------------------------------------------------------------------------

function __myIpAddress() {
	const ip = this.server.getRemoteIp( this.httpRequest );
	this.onSuccess(ip);
}


